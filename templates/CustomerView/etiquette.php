            <h2>Continuity</h2>
            <p>If your scene is in a Club and you have a glass of wine for a prop, do not actually drink from it. The scene may need to be filmed several times from a number of angles and abrupt changes in the level of wine in your glass could cause continuity problems for the editing crew. Simply mime drinking and make sure to hold the glass so that your hand covers the level of the fluid.</p>
            <p>This also applies to hair and clothing, body positioning or actions you are asked to do. Don’t change the object you are holding to another hand or swap your crossed legs if sitting etc all this could limit how the film can be cut together in the edit suite and limit the shots that can be used.</p>
